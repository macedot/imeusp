//-----------------------------------------------------------------------------
// CElem::CElem(TIPOELEM Tipo, uint32_t nlTime)
//-----------------------------------------------------------------------------
CElem::CElem(TIPOELEM Tipo, uint32_t nlTime)
{
  m_Tipo = Tipo;
  m_nEspera = nlTime;
}
//-----------------------------------------------------------------------------
// CElem::~CElem(void)
//-----------------------------------------------------------------------------
CElem::~CElem(void)
{
}
//-----------------------------------------------------------------------------
// void CElem::Atende(uint32_t nlTime)
//-----------------------------------------------------------------------------
void CElem::Atende(uint32_t nlTime)
{
  m_nEspera = (nlTime - m_nEspera);
  m_nAtende = nlTime;
}
//-----------------------------------------------------------------------------
// void CElem::Finaliza(uint32_t nlTime)
//-----------------------------------------------------------------------------
void CElem::Finaliza(uint32_t nlTime)
{
  m_nAtende = (nlTime - m_nAtende);
}
